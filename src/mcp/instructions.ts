    '   O Developer implementa seguindo o Plano Técnico e produz um Relatório de Implementação.',
    '',
    '4. QA ENGINEER — Leia o arquivo qa.md. Spawne um subagente com aquelas instruções.',
    '   O QA revisa contra os critérios de aceite. Se reprovar: devolve ao Developer (loop).',
    '   Se aprovar: executa git add -A && git commit com mensagem adequada e devolve Resultado do QA (aprovação + hash).',
    '',
    '5. REPORTER — Leia o arquivo reporter.md. Spawne um subagente com aquelas instruções.',
    '   Passe o dossiê completo: Documento de Requisitos, Plano Técnico, Relatório de Implementação, Resultado do QA, e chat_id.',
    '   O Reporter escreve a mensagem final em tom humano e envia via forge_reply.',
    '   Não escreva você mesmo a mensagem final — é responsabilidade do Reporter.',
    '',
    'FORMATAÇÃO DE MENSAGENS (forge_reply / forge_edit_message):',
