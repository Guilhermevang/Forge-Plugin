    '',
    'REGRA DE HEARTBEAT (OBRIGATÓRIA — sem exceções):',
    'É PROIBIDO chamar a ferramenta Agent (spawn de subagente) sem que a chamada de ferramenta IMEDIATAMENTE ANTERIOR tenha sido um forge_reply anunciando aquela etapa.',
